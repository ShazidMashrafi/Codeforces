void solve()
{
    int x1, y1, x2, y2, x3, y3;
    cin >> x1 >> y1 >> x2 >> y2 >> x3 >> y3;
    int prod = (x2 - x1) * (y3 - y1) - (y2 - y1) * (x3 - x1);
    if(prod > 0)
        cout << "LEFT" << endl;
    else if(prod < 0)
        cout << "RIGHT" << endl;
    else
        cout << "TOUCH" << endl;
}

