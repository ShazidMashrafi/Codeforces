ll w, h, n;

bool good(ll x)
{   
    return (x / w) * (x / h) >= n;
}

void solve()
{
    cin >> w >> h >> n;
    ll l = 0; // l is bad
    ll r = 1; // r is good

    while(!good(r))  r *= 2;

    while(r > l + 1)
    {
        ll m = (l + r) / 2;
        if(good(m))
            r = m;
        else
            l = m;
    }
    cout << r << endl;
}

