void solve()
{
    ll n, t;
	cin >> n >> t;
	vector<ll>v(n);
	for(auto &i : v)	cin >> i;
 
	int mx = 0, sum = 0;
	int i = 0, j = 0;
	while(i < n) {
		sum += v[i];
		i++;
		
		while(sum > t) {
			sum -= v[j];
			j++;
		}
		mx = max(mx, i - j);
	}
	cout << max(mx, i - j) << endl;
}

