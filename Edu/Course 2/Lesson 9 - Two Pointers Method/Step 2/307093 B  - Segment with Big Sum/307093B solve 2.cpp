void solve()
{
    ll n, s;
    cin >> n >> s;
    vector<int> a(n);
    for(auto &i : a)    cin >> i;

    ll l = 0, x = 0, res = INT_MAX;
    for(ll r = 0; r < n; r++) {
        x += a[r];
        while(x >= s) {
            x -= a[l];
            l++;
        }
        if(l > 0)
            res = min(res, r - l + 2);
    }
    if(res == INT_MAX)
        res = -1;
    cout << res << endl;
}

